package gpu

import chisel3._
import chisel3.util._

/** Special Function Unit (SFU)
  *
  * 实现超越函数计算，目前支持：
  *   - EXP: e^x (使用查找表 + 线性插值)
  *
  * 设计参考真实 GPU：
  *   - NVIDIA GPU: 每个 SM 有 4 个 SFU，延迟 16-32 周期
  *   - 本实现: 简化版，延迟 3 周期（流水线）
  *
  * 数据格式：定点数 Q16.16 (16位整数 + 16位小数)
  *   - 输入范围: [-8, 8]
  *   - 输出范围: [e^-8, e^8] ≈ [0.000335, 2981]
  *
  * 实现方法：
  *   1. 查找表：存储 65 个关键点 (e^-8 到 e^8，步长 0.25)
  *   2. 线性插值：result = v0 + (v1 - v0) * frac
  *   3. 流水线：3 级 (查表 -> 插值 -> 输出)
  */
class SFU(dw: Int = GpuParams.DataWidth) extends Module {
  val io = IO(new Bundle {
    val op = Input(UInt(4.W)) // 操作码
    val x = Input(SInt(dw.W)) // 输入 (Q16.16 定点数)
    val valid = Input(Bool()) // 输入有效
    val result = Output(SInt(dw.W)) // 输出 (Q16.16 定点数)
    val done = Output(Bool()) // 输出有效
  })

  // ============================================================
  // 查找表：e^x 的值，x 从 -8 到 8，步长 0.25
  // ============================================================
  // 格式：Q16.16 定点数
  // 例如：e^0 = 1.0 = 0x00010000 (65536)
  //       e^1 = 2.718 = 0x0002B7E1 (178145)

  val expLUT = VecInit(
    Seq(
      // e^-8.00 到 e^-7.00 (步长 0.25)
      0x00000015L, // e^-8.00  ≈ 0.000335
      0x0000001cL, // e^-7.75  ≈ 0.000431
      0x00000024L, // e^-7.50  ≈ 0.000553
      0x0000002eL, // e^-7.25  ≈ 0.000710
      0x0000003bL, // e^-7.00  ≈ 0.000912

      // e^-6.75 到 e^-5.75
      0x0000004cL, // e^-6.75  ≈ 0.001171
      0x00000062L, // e^-6.50  ≈ 0.001503
      0x0000007eL, // e^-6.25  ≈ 0.001930
      0x000000a2L, // e^-6.00  ≈ 0.002479
      0x000000d0L, // e^-5.75  ≈ 0.003183

      // e^-5.50 到 e^-4.50
      0x0000010bL, // e^-5.50  ≈ 0.004087
      0x00000157L, // e^-5.25  ≈ 0.005248
      0x000001b9L, // e^-5.00  ≈ 0.006738
      0x00000236L, // e^-4.75  ≈ 0.008652
      0x000002d8L, // e^-4.50  ≈ 0.011109

      // e^-4.25 到 e^-3.25
      0x000003a6L, // e^-4.25  ≈ 0.014264
      0x000004b0L, // e^-4.00  ≈ 0.018316
      0x00000605L, // e^-3.75  ≈ 0.023518
      0x000007bbL, // e^-3.50  ≈ 0.030197
      0x000009edL, // e^-3.25  ≈ 0.038774

      // e^-3.00 到 e^-2.00
      0x00000cbeL, // e^-3.00  ≈ 0.049787
      0x0000105dL, // e^-2.75  ≈ 0.063928
      0x00001503L, // e^-2.50  ≈ 0.082085
      0x00001afbL, // e^-2.25  ≈ 0.105399
      0x000022a5L, // e^-2.00  ≈ 0.135335

      // e^-1.75 到 e^-0.75
      0x00002c7cL, // e^-1.75  ≈ 0.173774
      0x0000391fL, // e^-1.50  ≈ 0.223130
      0x00004958L, // e^-1.25  ≈ 0.286505
      0x00005e2dL, // e^-1.00  ≈ 0.367879
      0x000078edL, // e^-0.75  ≈ 0.472367

      // e^-0.50 到 e^0.50
      0x00009b45L, // e^-0.50  ≈ 0.606531
      0x0000c75fL, // e^-0.25  ≈ 0.778801
      0x00010000L, // e^0.00   = 1.000000
      0x000148b5L, // e^0.25   ≈ 1.284025
      0x0001a612L, // e^0.50   ≈ 1.648721

      // e^0.75 到 e^1.75
      0x00021df3L, // e^0.75   ≈ 2.117000
      0x0002b7e1L, // e^1.00   ≈ 2.718282
      0x00037d87L, // e^1.25   ≈ 3.490343
      0x00047b4fL, // e^1.50   ≈ 4.481689
      0x0005c12dL, // e^1.75   ≈ 5.754603

      // e^2.00 到 e^3.00
      0x00076399L, // e^2.00   ≈ 7.389056
      0x00097cdcL, // e^2.25   ≈ 9.487736
      0x000c2eb7L, // e^2.50   ≈ 12.182494
      0x000fa483L, // e^2.75   ≈ 15.642632
      0x001415e5L, // e^3.00   ≈ 20.085537

      // e^3.25 到 e^4.25
      0x0019ca53L, // e^3.25   ≈ 25.790340
      0x00211d8eL, // e^3.50   ≈ 33.115452
      0x002a8565L, // e^3.75   ≈ 42.521082
      0x00369920L, // e^4.00   ≈ 54.598150
      0x00461afcL, // e^4.25   ≈ 70.105412

      // e^4.50 到 e^5.50
      0x005a0462L, // e^4.50   ≈ 90.017131
      0x00739593L, // e^4.75   ≈ 115.584285
      0x009469c4L, // e^5.00   ≈ 148.413159
      0x00be90f6L, // e^5.25   ≈ 190.566268
      0x00f4b122L, // e^5.50   ≈ 244.691932

      // e^5.75 到 e^6.75
      0x013a30cfL, // e^5.75   ≈ 314.190660
      0x01936dc5L, // e^6.00   ≈ 403.428793
      0x02060348L, // e^6.25   ≈ 518.012825
      0x02992442L, // e^6.50   ≈ 665.141633
      0x03560f0bL, // e^6.75   ≈ 854.058763

      // e^7.00 到 e^8.00
      0x0448a216L, // e^7.00   ≈ 1096.633158
      0x05801ad7L, // e^7.25   ≈ 1408.104848
      0x07100adbL, // e^7.50   ≈ 1808.042414
      0x09119289L, // e^7.75   ≈ 2321.572415
      0x0ba4f53eL // e^8.00   ≈ 2980.957987
    ).map(_.S(dw.W))
  )

  // ============================================================
  // 3 级流水线实现
  // ============================================================

  // 将输入 x (Q16.16) 映射到查找表索引
  val x_shifted = io.x + (8 << 16).S // x + 8 (Q16.16)
  val index_raw = (x_shifted >> 14).asUInt // (x + 8) * 4
  val index = Mux(index_raw > 64.U, 64.U, index_raw(6, 0)) // 限制为 7 位，最大 64

  // 提取小数部分用于插值
  val frac = x_shifted(15, 14)

  val s0Valid = RegNext(io.valid && io.op === GpuOpcode.EXP, false.B)
  val s0Index = RegNext(index, 0.U)
  val s0Frac = RegNext(frac, 0.U)

  val s1Valid = RegNext(s0Valid, false.B)
  val s1V0 = RegNext(expLUT(s0Index), 0.S)
  val s1V1 = RegNext(expLUT(Mux(s0Index === 64.U, 64.U, s0Index + 1.U)), 0.S)
  val s1Frac = RegNext(s0Frac, 0.U)

  val delta = s1V1 - s1V0
  val interp = (delta * s1Frac.asSInt) >> 2
  val expResult = s1V0 + interp

  val s2Valid = RegNext(s1Valid, false.B)
  val s2Result = RegNext(Mux(s1Valid, expResult, 0.S), 0.S)

  io.result := Mux(s2Valid, s2Result, 0.S)
  io.done := s2Valid
}
